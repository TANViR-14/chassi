<a1>নিড়ানি ও পাতলাকরণ:</a1><a2> বাংলাদেশের পাট ক্ষেতে ৩৯টি গোত্রের অন্তর্গত ৯৯টি গণ এর ১২৯টি প্রজাতির উদ্ভিদ আগাছা হিসেবে জন্মে। এদের মধ্যে ২৭টি প্রজাতি আগাছা হিসেবে খুব গুরুত্বপূর্ণ। এদের ঘনত্ব প্রতি ৩ বর্গফুটে ৫ থেকে ১০২টি আগাছা হতে পারে। পাটের আবাদে যে খরচ হয় তার একটা বড় অংশই নিড়ানি ও পরিচর্যার জন্য হয়। চারা গজানোর ১৫-২০ দিনের মধ্যে চারা পাতলা করা প্রয়োজন। সারিতে বপন করা হলে হাত দিয়ে বা ছিটিয়ে বপন করা হয়ে থাকলে আঁচড়া দিয়ে প্রাথমিকভাবে চারা পাতলা করা যায়। পাট গাছের বয়সের ৪০-৫০ দিনের মধ্যে একবার নিড়ানি দেয়ার সময় কৃষক কোনো কোনো ক্ষেতে চারা ও পাতলা করেন। তবে বয়স ৬০-৭০ দিনের মধ্যে যে চারা পাতলা করা হয় তা টানা বাছ নামে এবং বয়সের ৮৫-৯০ দিনের মধ্যে চারার গোড়া কেটে যে পাতলা করা হয় তা কাটা বাছ নামে পরিচিত। এ কচি গাছ না ফেলে পচিয়ে খুব উন্নত মানের পাট পাওয়া যায়, যা সুতা তৈরি করায় ব্যবহৃত হতে পারে।</a2><br>
