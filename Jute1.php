<a1>পানি নিষ্কাশন:</a1><a2> পাটের জমিতে জমে থাকা পানি নিকাশ অত্যন্ত জরুরি। জমিতে পানি জমলে মাটির ভেতর বাতাস প্রবেশ করতে পারে না ফলে গাছের শিকড়ের শ্বাস-প্রশ্বাস বিঘ্নিত হয়, গাছ স্বাভাবিকভাবে খাদ্য গ্রহণ করতে পারে না, ফলে গাছের দৈহিক বৃদ্ধি ঘটে না এবং শেষ পর্যন্ত গাছ মারা যায়। দেশী পাটের জাত চারা অবস্থায় পানি সহ্য ক্ষমতা থাকে না। তবে এ জাতের গাছের বয়স ও উচ্চতা বৃদ্ধির সাথে সাথে জলাবদ্ধতা সহ্য করতে পারে। কিন্তু তোষা পাট চারা অবস্থায় পানি সহ্য করতে পারে না, এমনকি বড় হলেও দাঁড়ান পানি বা জলাবদ্ধতা সহ্য করতে পারে না।</a2><br>
