<a1>পাটের কান্ডপচা রোগ:</a1>

<a2> চারা যখন ৬-৮ ইঞ্চি লম্বা হয় তখন থেকে শুরু করে পূর্ণ বয়স পর্যন্ত পাট, কেনাফ ও মেস্তাগাছ এ রোগে আক্রান্ত হয়। মে থেকে অক্টোবর পর্যন্ত এই রোগের প্রাদুর্ভাব দেখা যায়। পাতা ও কান্ডে গাঢ় বাদামী রং-এর দেখা দেয়। এ দাগ গোড়া থেকে গাছের আগা পর্যন্ত যে কোন অংশে দেখা দিতে পারে। ভালভাবে লক্ষ্য করলে দাগী জায়গা গুলোতে অসংখ্য কালো বিন্দু দেখা যায়। এ কালো বিন্দু গুলোতে ছত্রাক জীবাণু থাকে, এরা বাতাসে বা বৃষ্টির পানির মাধ্যমে আশে পাশের গাছে সংক্রমিত হয়। কখনও কখনও আক্রান্ত স্থানে গোটা গাছই ভেঙ্গে পড়ে। অর্থ্যাৎ গাছের চারা অবস্থা থেকে আরম্ভ করে পাট কাটা পর্যন্ত এ রোগের প্রকোপ দেখা যায়। কেনাফ ও মেস্তা ফসলেও এ রোগে আক্রমণাত্মক হয়।<br>
 

আক্রান্ত গাছগুলোকে তুলে দূরে মাটিতে পুতে ফেলে কিংবা পুড়ে ফেলে এ রোগ দমন করা যায়। ডাইথেন এম-৪৫, ম্যানার এম-৪৫, এনডোফিল এম-৪৫ ১০ লিটার পানিতে ২০ গ্রাম ঔষধ মিশিয়ে গাছের গোড়ার মাটিতে ২/৩ দিন পর পর স্প্রে মেশিনের সাহায্যে ছিটিয়ে এ রোগের আক্রমন কমানো সম্ভব।</a2><br>
