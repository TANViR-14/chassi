বীজের পরিমাণ ও গাছের ঘনত্ব প্রচলিত এবং জনপ্রিয় পদ্ধতি হচ্ছে ছিটিয়ে বপন করা। সারিতে বপন করা কষ্টসাধ্য ব্যয়বহুল মনে হলেও এর উপকারিতা বেশি। এতে জমির সর্বত্র গাছের বিস্তৃত সমভাবে থাকে, পরিচর্যার সুবিধা হয়। সারিতে বপন করা হলে বীজের পরিমাণ কম লাগে এবং গজায়ও ভালো। ৩০ সেন্টিমিটার দূরে দূরে সারি করে বীজ বপন করলে সবচেয়ে ভালো হয়। গাছ থেকে গাছের দূরত্ব সব সময় ঠিক রাখা না গেলেও পরিমিত অবস্থায় ৭ থেকে ১০ সেন্টিমিটার ধরা হয়। সারিতে বীজ বপন করার জন্য বীজ বপন যন্ত্র বা সিডার ব্যবহার লাভজনক।</a2><br>
