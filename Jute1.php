<br><a1>পাট কাটা:</a1><a2> বপনের ৬০-১২০ দিনের মধ্যে পাট ফসল কর্তন করা যায়, তবে আঁশের ফলন এবং মানের মধ্যে সমতা পেতে হলে ১০০-১২০ দিনের মধ্যেই পাট কাটার প্রকৃত সময়। সাধারণত পাট গাছে যখন ফুল আসা শুরু হয় তখন বা তার আগে প্রয়োজনমতো সুবিধাজনক সময় পাট ক্ষেত কাটা হয়। কৃষক অনেক সময় ফলন বেশি পাওয়ার জন্য দেরিতে পাট গাছ কাটেন।</a2><br>
