<a1>পাটের কালো-পট্রি রোগ:</a1><br>
<a2> কালো পট্রি রোগের লক্ষণ প্রায় কান্ড পচা রোগের মতই। তবে এতে কান্ডে কাল রং এর বেষ্টনীর মত দাগ পড়ে। আক্রান্ত স্থানে ঘষলে হাতে কালো গুড়ার মত দাগ লাগে। সাধারণতঃ গাছের মাঝামাঝি বয়স থেকে রোগ বেশী দেখা দেয়। এ রোগে গাছ ভেঙ্গে পড়ে না তবে গোটা গাছটি শুকিয়ে মরে যায়, ফলে আঁশ নিম্নমানের হয় ও ফলন কম হয়। সাধারণত তোষা পাটে এ রোগের প্রকোপ বেশী দেখা যায়। খরার সময় এ রোগের প্রাদুর্ভাব বেশী হয়। সাধারণত জুন মাস থেকে অক্টোবর  মাস পর্যন্ত এ রোগের প্রকোপ দেখা যায়।<br>
নীরোগ গাছ হতে বীজ সংগ্রহ করে শোধন করে নিলে এ রোগের প্রাদুর্ভাব কমে। ডাইথেন এম-৪৫, ম্যানার এম-৪৫, এনডোফিল এম-৪৫ ১০ লিটার পানিতে ২০ গ্রাম ঔষধ মিশিয়ে গাছের গোড়ার মাটিতে ২/৩ দিন পর পর স্প্রে মেশিনের সাহায্যে ছিটিয়ে এ রোগের আক্রমন কমানো সম্ভব।</a2><br>
 <br>

<a1>পাটের শুকনো ক্ষত রোগ:</a1>

<a2> এ রোগ শুধু দেশী পাট, কেনাফ ও মেস্তায় দেখা যায়। চারা অবস্থায় এ রোগ চারা মড়কের সৃষ্টি করে। বড় গাছের কান্ডে কালচে দাগ পড়ে। আক্রান্ত স্থান ফেটে যায়, ফাটা অংশ থেকে কখনও কখনও আঁশ ছিকড়ের মত বের হয়ে আসে। ক্ষতস্থানে জীবাণু সৃষ্টি হয়। এ জীবাণুগুলো বাতাসে উড়ে ফল আক্রমন করে। আক্রামত্ম ফল কালো ও আকারে ছোট হয়। এ ছত্রাকের বীজের স্থিতিশীলতা অনেক বেশী। বীজের অভ্যমত্মরে গভীরভাবে এ রোগের জীবাণু প্রবেশ করে এবং বীজকে প্রায় নষ্ট করে ফেলে। এ রোগে গাছ মরে না তবে আক্রান্ত অংশ শক্ত হয়। তাই পাট পচানোর পরেও আক্রান্ত স্থানের ছাল পাট কাঠির সাথে লেগে থাকে। এর ফলে আঁশ নিম্নমানের হয় এবং পাটের দামও অনেক কমে যায়। তোষা পাটে এ রোগ হয় না বলে জমিতে পর্যায়ক্রমে তোষা ও দেশী পাটের চাষ করলেও এ রোগের প্রকোপ অনেক কমে যায়। কান্ড-পচাঁ, শুকনো-ক্ষত ও কাল পট্রি এ তিনটি রোগই বীজ, মাটি বায়ুবাহী। এদের প্রতিকারের ব্যবস্থাও একই রকমের।
পাট কাটার পর জমির আগাছা, আবর্জনা ও পরিত্যক্ত গাছের গোড়া উপরে পুড়ে ফেলতে হবে।<br>
বীজ বপনের আগে বীজ শোধন করে নিতে হবে। বীজ শোধনের জন্য প্রোভেক্স-২০০ (০.৪%) এর ব্যবহারে যথেষ্ট সুফল দেয়। পাটের প্রধান ছত্রাকজনিত রোগগুলো বীজ ও মাটি বাহী। বপনের আগে বীজ শোধনের ফলে রোগের প্রকোপ অনেক কমে যায়। শোধন করা সম্ভব না হলে বপনের আগে বীজ রোদে ভালভাবে শুকাতে হবে।<br>
জমিতে চারা অবস্থা থেকে শুরম্ন করে ফসল কর্তন পর্যন্ত যখনই রোগের প্রকোপ দেখা দিবে তখনই রাসায়নিক ঔষধ ছিটানোর ব্যবস্থা করতে হবে। প্রথমে রোগাক্রামত্ম গাছ উঠিয়ে পুড়িয়ে ফেলতে হবে। পরে বেগতিক দেখলে প্রতি ১০ লিটার পানিতে ২০ গ্রাম ডাইথেন এম-৪৫ অথবা ম্যানার এম-৪৫ গুলে ৩-৪ দিন অমত্মর ২-৩ বার করে জমিতে ছিটাতে হবে। গাছের বয়স অনুসারে একর প্রতি ৩৫০-৪৫০ লিটার ঔষধ মিশানো পানি ছিটানো যেতে পারে।<br>
পাট পরবর্তী শস্য হিসাবে ধান, গম, আখ, সর্ষে, পিঁয়াজ, মটর, রসুন, মশুরী প্রভৃতির চাষ করা যেতে পারে। তিল, চিনাবাদাম, বরবটি, টমেটো, গোল আলু, পুঁইশাক, মূলা প্রভৃতি পাটের জমিতে পরবর্তী শস্য হিসাবে চাষ না করে রোগ দমনের জন্য সুপারিশকৃত শস্যক্রম পদ্ধতি অনুসরণ করা যেতে পারে।
নীরোগ পাট গাছ থেকে বীজ সংগ্রহ করতে হবে। গাছের ৭০% ফল পাকলেই বীজ কেটে ফেলা উচিত। দেরী করলে ছত্রাক সংক্রমিত হওয়ার সম্ভাবনা থাকে।
জমিতে সর্বদা পানি নিষ্কাশনের ব্যবস্থা করতে হবে।</a2><br><br>

<a1>পাট গাছের আগা শুকিয়ে যাওয়া রোগ:</a1><br>

<a2> সাধারণতঃ তোষা ও কেনাফ পাটে এ রোগ দেখা যায়। খরার পর ঝড়ে বা অন্য কোন কারণে গাছে আঘাত লাগলে এ রোগ বেশী হতে পারে। রোগে আক্রান্ত অংশ বাদামী রং এর হয় এবং আগা থেকে নীচের দিকে  শুকাতে থাকে। ফুল আসার পর সচরাচর এ রোগ দেখা দেয়।<br>
কান্ড পঁচা রোগের প্রতিকারের যে ব্যবস্থা অবলম্বন করা হয় এ ক্ষেত্রেও সেই একই ব্যবস্থা অবলম্বন করতে হবে। তবে তোষা পাটের জমিতে পর্যায়ক্রমে দেশী পাটের চাষ করলে এ রোগের প্রকোপ কম হতে পারে। রোগ দেখা দিলেই ডাইথেন এম-৪৫ নামক ছত্রাকনাশক ঔষধ ১৮.৫ গ্রাম প্রতি ১০ লিটার পানিতে মিশিয়ে ২/৩ দিন পরপর অমত্মত ২ বার গাছে স্প্রে করতে হবে।</a2><br><br>
<a1>পাট গাছের গোড়া পঁচা রোগ:</a1>
<br>
<a2> যে সমস্ত দেশী পাটের ক্ষেতে পানি নিষ্কাশন করা যায় না, সে সমসত্ম ক্ষেতের পাট গাছের গোড়ায় সাদা তুলার আঁশের মত এক প্রকার ছত্রাক রাতারাতি বেড়ে উঠে। কয়েকদিন পর সরিষার দানার মত বাদামী রংয়ের জীবাণুর দানা দেখা যায়। এ রোগের ফলে গাছের গোড়া পঁচে যায় এবং গাছ ভেঙ্গে পড়ে। জুন থেকে অক্টোবর মাস পর্যমত্ম এই রোগের প্রকোপ দেখা যায়। জমিতে এ রোগ দেখা দিলে গাছ কেটে আঁশ সংগ্রহ করা উচিত। দেশী ও তোষা উভয় পাটেই এই রোগের প্রাদুর্ভাব দেখা যায়।<br>
এ রোগের প্রতিকারের জন্য জমি পরিষ্কার পরিছন্ন এবং আর্বজনামুক্ত রাখতে হবে। পানি নিষ্কাশনের উপযুক্ত ব্যবস্থা করতে হবে। ডাইথেন এম-৪৫ প্রতি ১০ লিটার পানিতে ২০ গ্রাম মিশিয়ে গাছের গোড়ায় পর পর দু’দিন প্রয়োগ করলে রোগের ব্যাপকতা কমে যায়।</a2>
