<a1>০১. পর্যাপ্ত পানি থাকা অঞ্চল :</a1><a2> পর্যাপ্ত পানি থাকা অঞ্চলের জন্য পাট পচন প্রক্রিয়াকে কয়েকটি ভাগে ভাগ করা হয়েছে-</a2><br>
<a3>ক. বাছাইকরণ:</a3><a2> পাট কাটার পর ছোট, বড়, মোটা ও চিকন গাছকে পৃথক করে আলাদা আলাদা আঁটি বাঁধতে হয়। একটি আটির ওজন ১০ কেজির হবে। ছোট ও চিকন এবং মোটা ও বড় পাটের গাছেরগুলোকে পৃথক পৃথকভাবে জাক দিতে হবে। পাটের আঁটি কখনও খুব শক্ত করে বাঁধা যাবে না। এতে পাট পচতে বেশি সময় লাগে। কারণ শক্ত আঁটির মাধ্যে পানি পচনকালে ক্ষুদ্র ক্ষুদ্র ব্যাকটেরিয়া বা জীবাণু ভালোভাবে প্রবেশ করতে পারে না।</a2><br>
<a3>খ. পাতা ঝরান :</a3><a2> আঁটি বাঁধা শেষ হলে সেগুলোকে ৩-৪ দিন জমির ওপর স্তূপ করে রাখতে হয়। এ সময়ের মধ্যে পাতা ঝরে যাবে এবং গাছগুলো কিছুটা শুকিয়ে যাবে। পাতাগুলোকে জমির ওপরে ছড়িয়ে দিতে হয়। পাট গাছগুলো কিছুটা শুকানো ফলে পানিতে ডুবানোর পর গাছের ভেতর তাড়াতাড়ি পানি প্রবেশ করতে পারে।</a2><br>
<a3>গ. গোড়া ডুবান অথবা গোড়া থেতলান:</a3><a2> আঁশ শুকানোর পর দেখা যায় গোড়ার দিকে কিছু অংশ ছালযুক্ত অবস্থায় রয়ে গেছে। এ ছালযুক্ত অংশকে কাটিং বলে। পাতা ঝরার পর পাট গাছের গোড়ার দিকে প্রায় ৪৫ সেন্টিমিটার বা দেড় ফুট পরিমাণ অংশ ৩-৪ দিন পানির নিচে ডুবিয়ে রাখতে হয়। এতে গোড়ার অংশ অনেক নরম হয়ে যাবে। পাট গাছের গোড়ার প্রায় ৪৫ সেন্টিমিটার বা দেড় ফুট পরিমাণ অংশ একটি কাঠের হাতুড়ির সাহায্যে সামান্য থেতলানোর পর আঁটিগুলোকে পানির নিচে ডুবিয়ে দিতে হয়।</a2><br>
<a3>ঘ. পানি নির্বাচন:</a3><a2> যে পানি খুব পরিষ্কার এবং তার মধ্যে অল্প স্রোত থাকে সে পানি পাট পচনের জন্য সবচেয়ে ভালো। কোনো নিকটবর্তীস্থান এ ধরনের পানি থাকলে সে পানিতে পাট পচনের ব্যবস্থা করতে হবে।</a2><br>
<a3>ঙ. জাক তৈরি ও ইউরিয়া ব্যবহার:</a3><a2> জাক তৈরির সময় পাটের আঁটিগুলোকে প্রথম সারিতে লম্বালম্বিভাবে সাজাতে হবে। দ্বিতীয় সারিতে আড়াআড়িভাবে সাজাতে হবে। তৃতীয় সারিতে আবার লম্বালম্বিভাবে সাজাতে হবে। এভাবে জাক তৈরি করলে পানি এবং পচন জীবাণু জাকের মধ্যে সহজে চলাফেরা করতে পারে। বদ্ধ পানিতে বা ছোট পুকুর বা ডোবার পাট পচালে ইউরিয়া সার ব্যবহার করতে হবে। এতে পাট তাড়াতাড়ি পচে এবং আঁশের রঙ ভালো হয়। প্রতি ১০০ আঁটি কাঁচা পাটের জন্য প্রায় ১ কেজি ইউরিয়া ব্যবহার করা যেতে পারে। ইউরিয়া সার কোনো পাত্রে গুলে পানিতে মিশেয়ে অথবা সরাসরি জাকের আঁটির সারিতে ছিটিয়ে দিতে হবে।</a2><br>
<a3>চ. পচন নির্ণয়:</a3><a2> পাট পচনের শেষ সময় ঠিক করা অর্থাৎ পচন সমাপ্তি নির্ণয় অত্যন্ত প্রয়োজনীয়। পাট খুব বেশি পচলে আঁশ নরম হয় আবার খুব কম পচলে আঁশের গায়ে ছাল লেগে থাকে। কাজেই এমন সময় পচন থামাতে হবে যখন আঁশগুলো একটার সাথে আর একটা না লেগে থাকে কিন্তু শক্ত থাকে। জাক দেয়ার ৮-১০ দিন পর থেকে জাক পরীক্ষা করা উচিত। ২-৩টি পাট জাক থেকে বের করে ধুয়ে আঁশ পরীক্ষা করলে পচনের শেষ সময় ঠিক করা যায়। পচা পাটের মধ্যাংশ থেকে ১ ইঞ্চি বা আড়াই সেন্টিমিটার পরিমাণ ছাল কেটে ছোট শিশির ভেতর পানি দিয়ে ঝাঁকানোর পর শিশির পানি ফেলে আবার পরিষ্কার পানি দিয়ে ঝাঁকিয়ে যদি দেখা যায় যে আঁশগুলো বেশ পৃথক হয়ে গেছে তখন তখন বুঝতে হবে যে পচন শেষ হয়েছে। বেশি পচনের চেয়ে একটু কম পচন ভালো।</a2><br>
