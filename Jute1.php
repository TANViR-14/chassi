<a2> এ রোগে আক্রান্ত গাছের শিকড়ে ছোট বড় অনেক গিট দেখা যায়। এ গিটের ভিতর এক প্রকার কৃমিকীট (নিমোটোড) অবস্থান করে এবং গাছের ক্ষতি সাধন করে। এরা বিশেষ করে গাছের শিকড়ে ছত্রাক আক্রমণের অনেক সুবিধা করে দেয়। গাছের বাড় কমে যায় এবং ফুল কম ধরে। বালিযুক্ত হালকা মাটি বেশী দিন ভিজা থাকলে জমিতে পাটের শিকড় গিট রোগ দেখা যায়। দেশী ও তোষা উভয় পাটেই এই রোগের প্রকোপ দেখা যায়।<br>
যে সমস্ত পাটের জমিতে এ রোগ দেখা দেয়, পরবর্তী রবি মৌসুমে ঐ সব জমিতে সরিষা, গম, যব, ভুট্রা, চিনা প্রভৃতি এবং খরিফে ধান, কাউন, শন, জোয়ার ইত্যাদি বপন করা হলে রোগ জীবাণু কমতে থাকে। রাসায়নিক ঔষধ ফুরাডান-৫ জি, ৪০ কেজি প্রতি হেক্টরে প্রয়োগ করে নিড়ানী এবং চুড়ামত্মভাবে গাছ পাতলা করে দিতে হবে। সম্ভব হলে জমি পতিত বা পর্যায়ক্রমে মেসত্মার চাষ করা যেতে পারে, বপনের বেশ কিছু দিন আগে চাষ করে জমি রোদে ফেলে রাখতে হবে।
পাটের পাতার হলদে-সবুজ ছিট পড়া বা পাতার মোজাইক রোগ<br>

মোজাইক আক্রান্ত পাট গাছের সংগৃহীত বীজ বপনের ফলে এ রোগ হয়ে থাকে। গাছ বড় হওয়ার সাথে সাথে পাতায় হলদে সবুজ ছিট দাগ পড়ে, আক্রান্ত গাছের বাড় কমে যায় এবং আঁশের পরিমাণ শতকরা ৫০% পর্যমত্ম কমে যাওয়ার আশংকা থাকে। মোজাইক আক্রান্ত পাট গাছের সংগৃহীত বীজ বপনের ফলে এ রোগ হয়ে থাকে। পরাগায়ণের মাধ্যমে এবং হোয়াইট ফ্লাই (সাদা মাছি) নামক এক প্রকার ক্ষুদ্র মাছি দ্বারা এ রোগ বিস্তার লাভ করে। সাদা মাছি আক্রান্ত গাছের রস খেয়ে সুস্থ গাছের পাতার রস খাওয়ার সময় এ জীবাণু সুস্থ গাছে সংক্রমিত হয়।<br>
প্রতিরোধ ব্যবস্থা হিসাবে নীরোগ গাছ থেকে বীজ সংগ্রহ করা উচিত। জমিতে আক্রান্ত চারা দেখা মাত্র তা তুলে ফেলতে হবে। কোন ক্রমেই হলদে সবুজ ছিট পড়া আক্রান্ত গাছ জমিতে রাখা যাবে না। সুস্থ গাছের জন্য মাঝে মাঝে পাট ক্ষেতে ডায়াজিনন বা হেমিথ্রিন প্রতি ১০ লিটার পানিতে ১৫ মিলি পরিমাণ ঔষধ মিশ্রণ তৈরী করে ৩০-৪০ দিন বয়সের গাছে ৭ দিন পর পর ২-৩ বার ছিটানো উচিত। পাট গাছের মাঝামাঝি বয়সের বাড়ন্তকালে যদি এ রোগ ব্যাপকভাবে দেখা দেয় তা হলে ঐ ক্ষেতের পাট গাছ থেকে আঁশ সংগ্রহ করা চলতে পারে। কিন্তু বীজ সংগ্রহ করা একেবারেই নিষিদ্ধ। আক্রান্ত গাছের বীজ সংগ্রহ করে বপন করলে পরবর্তী বছর ব্যাপকভাবে এ রোগ দেখা দেবে।</a2><br><br>
