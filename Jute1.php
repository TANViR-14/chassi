জমি আগাছা মুক্ত রাখতে হবে। আক্রামত্ম জমিতে ২-৩ বছর তোষা পাটের আবাদ না করে দেশী পাটের আবাদ করা যেতে পারে। পাট কাটার পর গাছের গোড়া, শিকড় ও অন্যান্য পরিত্যাক্ত অংশ সংগ্রহ করে পুড়িয়ে ফেলতে হবে। কেনাফ এবং মেস্তাতেও এ রোগ হয়।
আক্রান্ত জমিতে পানি জমা থাকলে তা সরিয়ে ফেলতে হবে।</a2> <br>
