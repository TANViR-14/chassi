<a2> তিন ধরনের মাজরা পোকা বাংলাদেশের ধান ফসলের ক্ষতি করে। যেমন- হলুদ মাজরা। কালো মাথা মাজরা এবং গোলাপী মাজরা । মাজরা পোকার কীড়াগুলো কান্ডের ভেতরে থেকে খাওয়া শুরু করে এবং ধীরে ধীরে গাছের ডিগ পাতার গোড়া খেয়ে কেটে ফেলে। ফলে ডিগ পাতা মারা যায়। একে  ‘মরা ডিগ’ বা ‘ডেডহার্ট ’ বলে। গাছে শীষ আসার পূর্ব পর্যন্ত এ ধরনের ক্ষতি হলে মরা ডিগ দেখতে পাওয়া যায়। থোড় আসার আগে মরা ডিগ দেখা দিলে বাড়তি কিছু কুশী উৎপাদন করে গাছ আংশিকভাবে ক্ষতি পূরণ করতে পারে। <br>

ক্রিসেক রোগের অথবা ইঁদুরের ক্ষতির নমুনার সাথে মাঝে মাঝে মাজরা পোকা দ্বারা সৃষ্ট ক্ষত মরা ডিগ বলে ভুল হতে পারে। মরা ডিগ টান দিলেই সহজে উঠে আসে। এ ছাড়া ক্ষতিগ্রস্ত গাছের কান্ডে মাজরা পোকা খাওয়ার দরুণ ছিদ্র এবং খাওয়ার জায়গায় পোকার মল দেখতে পাওয়া যায়। <br>
