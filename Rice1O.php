পাতা ব্লাস্ট এর ক্ষেত্রে
পাতায় প্রথমে ডিম্বাকৃতির ছোট ছোট ধূসর বা সাদা বর্ণের দাগ দেখা যায়। দাগগুলোর চারদিক গাঢ় বাদামি বর্ণের হয়ে থাকে। পরবর্তীতে এ দাগ ধীরে ধীরে বড় হয়ে চোখের আকৃতি ধারণ করে। অনেক দাগ একত্রে মিশে পুরো পাতাটাই মেরে ফেলতে পারে। এ রোগে ব্যাপকভাবে আক্রান্ত হলে জমিতে মাঝে মাঝে পুড়ে যাওয়ার মতো মনে হয়। অনেক ক্ষেত্রে খোল ও পাতার সংযোগস্থলে কাল দাগের সৃষ্টি হয়। যা পরবর্তীতে পচে যায় এবং পাতা ভেঙে পড়ে ফলন বিনষ্ট হয়।। <br>


গিঁট বা নোড ব্লাস্ট এর ক্ষেত্রে
ধান গাছের থোড় বের হওয়ার পর থেকে এ রোগ দেখা যায়। গিঁটে কালো রঙের দাগ সৃষ্টি হয়।  ধীরে ধীরে এ দাগ বেড়ে গিঁট পচে যায়, ফলে ধান গাছ গিঁট বরাবর ভেঙে পড়ে। <br>
 

নেক বা শীষ ব্লাস্ট এর ক্ষেত্রে
এ রোগ হলে শীষের গোড়া অথবা শীষের শাখা প্রশাখার গোড়ায় কাল দাগ হয়ে পচে যায়। শীষ অথবা শীষের শাখা প্রশাখা ভেঙে পড়ে। ধান চিটা হয়। <br>

রোগের প্রতিকারে
রোগ প্রতিরোধী জাত ব্যবহার করতে হবে।
মাটিতে জৈব সারসহ সুষম মাত্রায় সব ধরনের সার ব্যবহার করতে হবে। আক্রান্ত জমির খড়কুটা পুড়িয়ে ফেলতে হবে এবং ছাই জমিতে মিশিয়ে দিতে হবে। সুস্থ গাছ হতে বীজ সংগ্রহ করতে হবে, দাগি বা অপুষ্ট বীজ বেছে ফেলে দিয়ে সুস্থ বীজ ব্যবহার করতে হবে। রোগের আক্রমণ হলে জমিতে ইউরিয়া সারের উপরিপ্রয়োগ বন্ধ রাখতে হবে। জমিতে সব সময় পানি রাখতে হবে। রোগের শুরুতে হেক্টরপ্রতি ৪০ কেজি (বিঘাপ্রতি ৫ কেজি পটাশ সার উপরিপ্রয়োগ করতে হবে। ট্রাইসাইক্লাজল (ট্রুপার ৭৫ ডব্লিউপি) বা টেবুকোনাজল + ট্রাইফ্লক্সিস্ট্রবিন (নাটিভো৭৫ ডব্লিউপি) প্রতি লিটার পানিতে ১ গ্রাম হারে মিশিয়ে ১০-১৫ দিন পর পর ২-৩ বার স্প্রে করতে হবে।

