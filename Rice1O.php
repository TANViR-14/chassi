<a1>বাদামি দাগ রোগ:</a1>

<a2> বাইপোলারিস ওরাইজি (Bipolaris oryzae) নামক ছত্রাক দ্বারা হয়ে থাকে।
এ রোগটি চারা অবস্থা থেকে যে কোনো বয়সের ধান গাছে হতে পারে। তবে চারার বয়স বেশি হলে রোগটির প্রকোপ বেশি দেখা যায়। মাটিতে পুষ্টি উপাদানের অভাব বা পানির অভাব হলে রোগের মাত্রা বেড়ে যায়। <br>

 
 
পাতায় প্রথমে তিলের দানার মতো ছোট ছোট দাগ পড়ে। দাগগুলো বড় হয়ে মাঝখানে সাদা ও কিনারা বাদামি হয়ে যায়।
একাধিক দাগ মিলে বড় দাগ সৃষ্টি হয়ে পাতাটিকে মেরে ফেলতে পারে। ধানের পাতার চেয়ে রোগটি বীজে বেশি দেখা যায়। রোগ আক্রান্ত গাছে অপুষ্ট বীজ হয় ও বাদামি বর্ণ হয়। <br>
 

 সুস্থ বীজ বপন করতে হবে; বীজ গরম পানিতে (৫০ ডিগ্রি সে. তাপমাত্রায় ৩০ মিনিট ভিজে রাখতে হবে) শোধন করতে হবে; কার্বেন্ডাজিম (অটোস্টিন) অথবা কার্বোক্সিন + থিরাম (প্রোভ্যাক্স ২০০ ডব্লিউপি) প্রতি কেজি বীজে ২.৫ গ্রাম হারে মিশিয়ে শোধন করতে হবে; জৈব সার ব্যবহার করতে হবে; জমিতে সঠিক পরিমাণ নাইট্রোজেন ও পটাশ সার ব্যবহার করলে রোগ কমে যায়; সঠিক পানি ব্যবস্থাপনা করতে হবে; জমিতে রোগ দেখা দিলে কার্বেন্ডাজিম (অটোস্টিন) প্রতি লিটার পানিতে ১.৫ গ্রাম হারে মিশিয়ে স্প্রে করতে হবে।</a2> <br>
