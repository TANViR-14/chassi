<a1>ব্লাস্ট রোগ:</a1>

<a2> ধান গাছের ৩টি অংশে রোগটি আক্রমণ করে থাকে। গাছের আক্রান্ত অংশের ওপর ভিত্তি করে এ রোগ তিনটি নামে পরিচিত যেমন- ১. পাতা ব্লাস্ট, ২. গিট ব্লাস্ট এবং ৩. নেক/শীষ ব্লাস্ট। <br>


পাইরিকুলারিয়াগ্রিসিয়া (Pyricularia grisea) নামক ছত্রাক দ্বারা হয়ে থাকে।
এ রোগটি আমন ও বোরো উভয় মৌসুমেই হতে পারে। ধানের চারা অবস্থা থেকে ধান পাকার আগ পর্যন্ত যে কোনো সময় এ রোগটি হতে পারে। বীজ, বাতাস, কীটপতঙ্গ ও আবহাওয়ার মাধ্যমে ছড়ায়। রাতে ঠাণ্ডা, দিনে গরম ও সকালে পাতলা শিশির জমা হলে এ রোগ দ্রুত ছড়ায়। হালকা মাটি বা বেলেমাটি যার পানি ধারণক্ষমতা কম সেখানে রোগ বেশি হতে দেখা যায়। জমিতে মাত্রাতিরিক্ত ইউরিয়া সার এবং প্রয়োজনের তুলনায় কম পটাশ সার দিলে এ রোগের আক্রমণ বেশি হয়। দীর্ঘদিন জমি শুকনা অবস্থায় থাকলেও এ রোগের আক্রমণ হতে পারে। <br>

