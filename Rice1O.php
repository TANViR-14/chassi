 <a1>বাদামী গাছফড়িং:</a1>
<img id="myImg2" src="pics/foring.jpg" alt="বাদামী গাছফড়িং" style="width:100%;max-width:300px">

<a2> যে সমস্ত ধানের জাতে বাদামী গাছফড়িং প্রতিরোধ ক্ষমতা নেই সে সব জাতের ধানে এরা খুব তাড়াতাড়ি বংশ বৃদ্ধি করে, ফলে এ পোকার সংখ্যা এত বেড়ে যায় যে, আক্রান্ত ক্ষেতে বাজ পড়ার মত হপারবার্ণ - এর সৃষ্টি হয়। আক্রান্ত গাছগুলো প্রথমে হলদে এবং পরে শুকিয়ে মারা যায়। বাদামী গাছফড়িং গ্রাসিস্টান্ট, র‌্যাগেটস্টান্ট ও উইল্টেডস্টান্ট নামক ভাইরাস রোগ ছড়ায়। কিন্তু আমাদের দেশে এখনও এ সমস্ত রোগ দেখা যায়নি। লম্বা পাখাবিশিষ্ট পূর্ণবয়স্ক বাদামী ফড়িংগুলো প্রথমে ধান ক্ষেত আক্রান্ত করে। এরা পাতার খোলে এবং পাতার মধ্য শিরায় ডিম পাড়ে। ডিমগুলোর ওপর পাতলা চওড়া একটা আবরণ থাকে। ডিম ফুটে বাচ্চা (নিমফ) বের হতে ৭-৯ দিন সময় লাগে। বাচ্চাগুলো ৫ বার খোলস বদলায় এবং পূর্ণবয়¤ক ফড়িং এ পরিণত হতে ১৩-১৫ দিন সময় নেয়। প্রথম পর্যায়ের (ইন¯টার) বাচ্চাগুলোর রং সাদা এবং পরের পর্যায়ের বাচ্চাগুলো বাদামী। বাচ্চা থেকে পূর্ণবয়¯ক বাদামী গাছফড়িং ছোট পাখা এবং লম্বা পাখা বিশিষ্ট হতে পারে। ধানে শীষ আসার সময় ছোট পাখা বিশিষ্ট ফড়িং এর সংখ্যাই বেশী থাকে এবং স্ত্রী পোকাগুলো সাধারণত: গাছের গোড়ার দিকে বেশি থাকে। গাছের বয়স বাড়ার সাথে সাথে লম্বা পাখা বিশিষ্ট ফড়িং এর সংখ্যাও বাড়তে থাকে, যারা এক জায়গা থেকে আর এক জায়গায় উড়ে যেতে পারে। 
