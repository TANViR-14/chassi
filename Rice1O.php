শীষ আসার পর মাজরা পোকা ক্ষতি করলে সম্পূর্ণ শীষ শুকিয়ে যায়। একে ‘সাদা শীষ’, ‘মরা শীষ’ বা ‘হোয়াইট হেড’ বলে। খরায় বা ইঁদুরের ক্ষতির নমুনা হোয়াইট হেড-এর মত দেখা যেতে পারে। কীড়া যদি পাতার খোলের ভেতরে খায় এবং কান্ডের ভেতরের অংশ সম্পূর্ণভাবে কেটে না দেয় তাহলে ধানগাছের আংশিক ক্ষতি হয় এবং শীষের গোড়ার দিকের কিছু ধান চিটা হয়ে যায়।<br>

মাজরা পোকার আক্রমণ হলে, কান্ডের মধ্যে কীড়া, তার খাওয়ার নিদর্শন ও মল পাওয়া যায়, অথবা কান্ডের বাইরের রং বিবর্ণ হয়ে যায় এবং কীড়া বের হয়ে যাওয়ার ছিদ্র থাকে। গাছে মাজরা পোকার ডিমের গাদা দেখলে বুঝতে হবে গাছের ক্ষতি হওয়ার সম্ভাবনা আছে। হলুদ মাজরা পোকা পাতার ওপরের অংশে ডিম পাড়ে এবং গোলাপী মাজরা পোকা পাতার খোলের ভিতরের দিকে ডিম পাড়ে। হলুদ মাজরা পোকার ডিমের গাদার ওপর হালকা ধূসর রঙের একটা আবরণ থাকে। কালোমাথা মাজরা পোকার ডিমের গাদার ওপর মাছের আঁশের মত একটা সাদা আবরণ থাকে, যা ডিম ফোটার আগে ধীরে ধীরে গাঢ় রং ধারণ করে।<br>
 
মাজরা পোকার কীড়াগুলো ডিম থেকে ফুটে রেরুবার পর আস্তে আস্তে কান্ডের ভেতরে প্রবেশ করে। কীড়ার প্রথমাবস্থায় এক একটি ধানের গুছির মধ্যে অনেকগুলো করে গোলাপী ও কালোমাথা মাজরার কীড়া জড়ো হতে দেখা যায়। কিন্তু হলুদ মাজরা পোকার কীড়া ও পুত্তলীগুলো কান্ডের মধ্যে যে কোন জায়গায় পাওয়া যেতে পারে।

আলোর চার পাশে যদি প্রচুর মাজরা পোকার মথ দেখতে পাওয়া যায় তাহলে বুঝতে হবে ক্ষেতের মধ্যে মথগুলো ডিম পাড়া শুরু করেছে। <br>
<a2>দমন ব্যবস্থাপনা:</a2>
 নিয়মিতভাবে ক্ষেত পর্যবেক্ষণের সময় মাজরা পোকার মথ ও ডিম সংগ্রহ করে নষ্ট করে ফেললে মাজরা পোকার সংখ্যা ও ক্ষতি অনেক কমে যায়। থোর আসার পূর্ব পর্যন্ত হাতজাল দিয়ে মথ ধরে ধ্বংস করা যায়।<br>
  ক্ষেতের মধ্যে ডালপালা পুঁতে পোকা খেকো পাখির বসার সুযোগ করে দিলে এরা পূর্ণবয়স্ক মথ খেয়ে এদের সংখ্যা কমিয়ে ফেলে।<br>
   মাজরা পোকার পূর্ণ বয়স্ক মথের প্রাদুর্ভাব যখন বেড়ে যায় তখন ধান ক্ষেত থেকে ২০০-৩০০ মিটার দূরে আলোক ফাঁদ বসিয়ে মাজরা পোকার মথ সংগ্রহ করে মেরে ফেলা যায়। <br>
   যে সব অঞ্চলে হলুদ মাজরা পোকার আক্রমণ বেশী, সে সব এলাকায় সম্ভব হলে চান্দিনার (বি আর ১) মত হলুদ মাজরা পোকা প্রতিরোধ সম্পন্ন জাতের ধান চাষ করে আক্রমণ প্রতিহত করা যায়। <br>
   ধানের জমিতে শতকরা ১০-১৫ ভাগ মরা ডিগ অথবা শতকরা ৫ ভাগ মরা শীষ পাওয়া গেলে অনুমোদিত কীটনাশক   (যেমন- ডায়াজিনন ৬০ ইসি, কার্বোফুরান ৫জি, ফেনিট্রথিয়ন ৫০ ইসি ইত্যাদি) ব্যবহার করা।</a2><br>
