<a1>ব্যাকটেরিয়াজনিত পোড়া  রোগ:</a1><br>
<a2>জ্যানথোমোনাস অরাইজি পিভি অরাইজি (Xanthomonas oryzae pv.oryzae) এক ধরনের ব্যাকটেরিয়ার আক্রমণে হয়ে থাকে।<br>
 

রোগটি আউশ, আমন ও বোরো এ তিন মৌসুমেই ব্যাপকভাবে দেখা  যায়। উচ্চ তাপমাত্রা (২৬-৩০ ডিগ্রি সে.), ৭০% এর ওপরে আপেক্ষিক আর্দ্রতা, ঝড়ো-বৃষ্টি আবহাওয়া, রোগপ্রবণ জাত লাগানো,  রোপণের সময় শিকড় অথবা গাছে ক্ষত সৃষ্টি, উচ্চ মাত্রায় ইউরিয়া সার প্রয়োগ ইত্যাদির কারণে রোগের প্রকোপ বেশি হয়। পাতাপোড়া রোগ ধানের খড়, মাটি, পোকা, বাতাস ও সেচের পানির মাধ্যমে এক জমি থেকে অন্য জমিতে ছড়ায়।<br>


প্রথমে পাতার অগ্রভাগ বা কিনারায় নীলাভ পানিচোষা দাগ দেখা যায়। দাগগুলো আস্তে আস্তে হালকা হলুদ রঙ ধারণ করে পাতার অগ্রভাগ থেকে নিচের দিকে বাড়তে থাকে। শেষের দিকে আংশিক বা সম্পূর্ণ পাতা ঝলসে যায় এবং ধূসর বা শুকনো খড়ের রঙ ধারণ করে। ফলে গাছটি প্রথমে নেতিয়ে পড়ে ও আস্তে আস্তে পুরো গাছটি মরে যায়। চারা ও কুশি অবস্থায় সাধারণত ক্রিসেক লক্ষণ প্রকাশ পায়। এ রোগের ফলে গাছটি প্রথমে নেতিয়ে পড়ে ও আস্তে আস্তে পুরো গাছটি মরে যায়। অনেক সময় এ রোগের লক্ষণের অগ্রভাগ দিয়ে ব্যাকটিরিয়ার কোষগুলো বেরিয়ে আসে এবং কোষগুলো একত্রে মিলিত হয়ে ভোরের দিকে হলদে পুঁতির দানার মতো গুটিকা সৃষ্টি করে এবং এগুলো শুকিয়ে শক্ত হয়ে পাতার গায়ে লেগে থাকে। পরবর্তীকালে পাতার গায়ে লেগে থাকা জলকণা গুটিকাগুলোকে গলিয়ে ফেলে, ফলে এ রোগের জীবাণু অনায়াসে চারদিকে ছড়িয়ে পড়ে। আক্রান্ত গাছের কা- ছিড়ে চাপ দিলে দুর্গন্ধযুক্ত পুঁজের মতো তরল পদার্থ বের হয়। রাত ও দিনের তাপমাত্রার পার্থক্য বেশি (৮-১০ ডিগ্রি সে.) হলে কচি পাতায় ফ্যাকাশে হলুদ বর্ণের লক্ষণটি প্রকাশ পায়।<br>



রোগ প্রতিরোধী জাত চাষ করতে হবে। রোগাক্রান্ত জমির ধান কাটার পর নাড়া ও খড় পুড়িয়ে ফেলতে হবে। চারা উঠানোর সময় যেন শিকড় কম ছিড়ে সে ব্যাপারে সতর্ক থাকা। সুষম মাত্রায় সার ব্যবহার ও ইউরিয়া সার ৩/৪ কিস্তিতে প্রয়োগ করতে হবে। কিন্তু রোগ দেখার পর ইউরিয়া সার উপরিপ্রয়োগ বন্ধ করতে হবে। ক্রিসেক আক্রান্ত জমি শুকিয়ে ৫-১০ দিন পর আবার পানি দিতে হবে। রোগ দেখার পর বিঘাপ্রতি অতিরিক্ত ৫ কেজি পটাশ সার ছিপছিপে পানিতে প্রয়োগ করতে হবে। প্রতি লিটার পানিতে ১ গ্রাম হারে চিলিটেড জিংক স্প্রে করলে রোগের তীব্রতা কমে যায়।</a2><br>
