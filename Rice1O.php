 প্রায় ৪০ লিটার পরিষ্কার পানিতে দেড় কেজি ইউরিয়া সার মিশিয়ে তার মধ্যে বীজ ধান ছেড়ে দিয়ে নেড়েচেড়ে দিতে হব। ভারী, পুষ্ট, সুস্থ ও সবল বীজ পাত্রের তলায় জমা হবে এবং অপরিপুষ্ট, হালকা, রোগা বা ভাঙ্গা বীজ ভেসে থাকবে। হাত অথবা চালনি দিয়ে ভাসমান বীজগুলো পৃথক করে নিতে হবে। ভারী বীজ নীচ থেকে তুলে নিয়ে পরিষ্কার পানিতে ৩ থেকে ৪ বার ভাল করে ধুয়ে ছায়াতে শুকিয়ে অথবা এভাবে বাছাইকৃত বীজ জাগ দিয়ে মুখ ফুটিয়ে বীজতলায় বোনা যেতে পারে। তাছাড়া কুলা দিয়ে খুব ভালোভাবে ঝেড়ে নিয়েও পরিপুষ্ট বীজ বাছাই করা যায়।<br>
 বাণিজ্যিক ভাবে চাষাবাদের ক্ষেত্রে এবং ভালো সুষ্ঠ বীজ নির্বাচনের জন্য কৃষক, নমুনা বীজ মাঠ পর্যায়ে পরিক্ষা করতে পারেন, এ ক্ষেত্রে বীজ গজানোর হার ৮০% এর বেশী হবে।<br>

 সাধারন ভাবে ১ কেজি অংকুরিত বীজ ১ শতক বীজ তলার জন্যে এবং ১ শতাংশের বীজ তলা দিয়ে ২২-২৫ শতাংশ জমিতে রোপন করা যায়। তবে উচ্চ ফলনশীল জাতের ক্ষেত্রে বীজের প্যাকেটে লেখা নির্দেশিকা অবশ্যই মেনে চলতে হবে।<br>

বীজ শোধনঃ ভিটাভেক্স ২০০ / টিলথ অনুমোদিত মাত্রায় ব্যবহার করে বীজ় শোধন করা ভাল।</a2><br>
<br>
<a1>নিড়ানি ও পাতলাকরণ:</a1><a2> বাংলাদেশের পাট ক্ষেতে ৩৯টি গোত্রের অন্তর্গত ৯৯টি গণ এর ১২৯টি প্রজাতির উদ্ভিদ আগাছা হিসেবে জন্মে। এদের মধ্যে ২৭টি প্রজাতি আগাছা হিসেবে খুব গুরুত্বপূর্ণ। এদের ঘনত্ব প্রতি ৩ বর্গফুটে ৫ থেকে ১০২টি আগাছা হতে পারে। পাটের আবাদে যে খরচ হয় তার একটা বড় অংশই নিড়ানি ও পরিচর্যার জন্য হয়। চারা গজানোর ১৫-২০ দিনের মধ্যে চারা পাতলা করা প্রয়োজন। সারিতে বপন করা হলে হাত দিয়ে বা ছিটিয়ে বপন করা হয়ে থাকলে আঁচড়া দিয়ে প্রাথমিকভাবে চারা পাতলা করা যায়। পাট গাছের বয়সের ৪০-৫০ দিনের মধ্যে একবার নিড়ানি দেয়ার সময় কৃষক কোনো কোনো ক্ষেতে চারা ও পাতলা করেন। তবে বয়স ৬০-৭০ দিনের মধ্যে যে চারা পাতলা করা হয় তা টানা বাছ নামে এবং বয়সের ৮৫-৯০ দিনের মধ্যে চারার গোড়া কেটে যে পাতলা করা হয় তা কাটা বাছ নামে পরিচিত। এ কচি গাছ না ফেলে পচিয়ে খুব উন্নত মানের পাট পাওয়া যায়, যা সুতা তৈরি করায় ব্যবহৃত হতে পারে।</a2><br>
<br>
<a1>জমি চাষ ও বীজ তলা তৈরীঃ</a1><a2> মাটির প্রকার ভেদে প্রয়োজনমত ৩-৪ টি  চাষ এবং মই দিয়ে জমি সমান করতে হবে এবং থকথকে কাদা করতে হবে।<br>

জমি উঁচু নীচু থাকলে মই ও কোদাল দিয়ে সমতল করতে হবে।<br>

প্রথম চাষের পর অন্তত ৭ দিন পর্যন্ত জমিতে পানি আটকে রাখুন। এতে জমির খড় ও আগাছা পচে জমি উর্বর বেসকরবে।<br>
 চারটি পদ্ধতিতে বীজতলা তৈরি করা যায়। এগুলো হচ্ছে শুকনো, কাদাময়, ভাসমান ও ডাপোগ বীজতলা। শুকনো বীজতলা- উপযুক্ত আর্দ্রতায় ৫-৬ বার চাষ ও মই দিয়ে মাটিকে একেবারে ঝুরঝুরে করার পর জমি সমান করে শুকনো বীজতলা তৈরি করা যায়।দোঁআশ ও এটেল মাটি এ বীজতলায় জন্য ভাল। বীজতলার জমি উর্বর হওয়া প্রয়োজন। যদি জমি হয় তাহলে প্রতি বর্গমিটার জমিতে দুই কেজি হারে জৈব সার (পচা গোবর বা আবর্জনা) সুন্দরভাবে ছড়িয়ে দিতে হবে। এরপর জমিতে ২-২.৫ ইঞ্চি পানি দিয়ে দু-তিনটি চাষ ও মই দিয়ে ৭ থেকে ১০ দিন রেখে দিন এবং পানি ভালভাবে আটকে রাখুন। আগাছা, খড় ইত্যাদি পচে গেল আবার চাষ ও মই দিয়ে থকথকে কাদাময় করে জমি তৈরি করতে হবে। এবার জমির দৈর্ঘ্য বরাবর ১ মিটার চওড়া বেড তৈরি করতে হবে। বেডের দুই পাশের মাটি দিয়ে বেড তৈরি করা যায়।এরপর বেডের উপরের মাটি বাঁশ বা কাঠের চ্যাপ্টা লাঠি দিয়ে সমান করতে হবে। কাদা বেশি হলে বীজ মাটিতে ডুবে যাবে এবং তাতে ভালভাবে বীজ গজাবে না। এ অবস্থায় বেড তৈরির পৌনে এক ঘন্টা পর বীজ বোনা দরকার।<br>

বীজ তলার চার পাশে বেড়া/নেট দিয়ে ঘেরে দিতে হবে। সেচের প্রয়োজন হলে পরিমিত মাত্রায় সেচ দিতেন হবে।<br>



সাধারণভাবে বীজ তলার বয়স আউশে ২০-২৫ দিন, রোপা আমনে ২৫-৩০ দিন(বীজ তলায় বীজ বপন-আষাঢ় মাস)এবং বোরাতে ৩৫-৪৫ দিন(বীজ তলায় বীজ বপন-১লা কার্তিক থেকে অগ্রাহয়ণের ১ম সপ্তাহ) হওয়া উচিত। তবে কোম্পানীর উৎপাদিত বীজের ক্ষেত্রে প্যাকেটের গায়ে লেখা  নির্দেশকা অবশ্যই মেনে চলতে হবে।<br>

ছিটিয়ে বা লাইনে বপন করতে রোপা ধান সাধারনত লাইনে রোপন করা ভালো এতে করে ফসলে আলো বাতাসের প্রবেশ যেমন বৃদ্ধি পায় তেমনি অন্তবর্তীকালীন পরিচর্যা করতে অনেক সুবিধা হয়।<br>

রোপনের ক্ষেত্রে সাধারনত লাইন থেকে লাইনের দুরত্ব ৯-১০ ইঞ্চি এবং প্রতি লাইনে গোছা/থোর থেকে গোছা/থোরের দুরত্ব ৬ ইঞ্চি। তবে

বিভিন্ন কোম্পানীর উৎপাদিত বীজের ক্ষেত্রে প্যাকেটের গায়ে লেখা নির্দেশকা অবশ্যই মেনে চলতে হবে।</a2><br>


