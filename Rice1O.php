<a2> উরচুংগা গাছের গোড়া কেটে দেয়, ফলে গাছ মারা যায়। অনেক সময় এদের ক্ষতি মাজরা পোকার ক্ষতির সাথে ভুল হতে পারে। উরচুংগা গাছের নতুন শিকড় এবং মাটির নীচে গাছের গোড়া খেয়ে ফেলে। কিন্তু মাজরা পোকা কান্ডের ভেতরটা খায়। পানি আটকে রাখা যায় না এমন ধান ক্ষেতে উরচুংগা একটা সমস্যা। এ পোকা তখনই আক্রমণ করে যখন ক্ষেতে আর পানি থাকে না, অথবা স্থানে স্থানে যখন পানি কম বেশী হয় এবং মাটি দেখা যায়। ক্ষেত পানি দিয়ে ডুবিয়ে দিলে উরচুংগা আইলে বা উঁচু জায়গায় চলে যায়। সেখানে মাটির নীচে শক্ত স্থানে ডিম পাড়ে। </a2><br>
