<a1>পামরী পোকা:</a1>
<img id="myImg2" src="pics/pamori.jpg" alt="পামরী পোকা" style="width:100%;max-width:300px">

<a2> পূর্ণবয়স্ক পামরী পোকার গায়ের রং কালো এবং পিঠে কাঁটা আছে। পূর্ণবয়স্ক ও তাদের কীড়াগুলো উভয়ই ধান গাছের ক্ষতি করে। পূর্ণবয়স্ক পামরী পোকা পাতার সবুজ অংশ কুরে কুরে খাওয়ার ফলে ক্ষতিগ্রস্ত পাতার ওপর লম্বালম্বি কয়েকটি সমান্তরাল দাগ দেখতে পাওয়া যায়। বেশী ক্ষতিগ্রস্থ ক্ষেতের পাতাগুলো শুকিয়ে পুড়ে যাওয়ার মত মনে হয়। এরা পাতার উপরের সবুজ অংশ এমন ভাবে খায় যে শুধু নীচের পর্দাটা বাকী থাকে। একটি ক্ষতিগ্রস্ত ক্ষেতে অনেক পূর্ণ বয়স্ক পামরী পোকা দেখা  যেতে পারে। পূর্ণ বয়স্ক পোকাগুলো পূর্ববর্তী ধান ফসল থেকে নতুন ক্ষেত আক্রমণ করে। সাধারণতঃ বাড়ন্ত  গাছ আক্রান্ত বেশী হয় এবং ধান পাকার সময় পোকা থাকে না। স্ত্রী পামরী পোকা পাতার নীচের দিকে ডিম পাড়ে। কীড়াগুলো পাতার দুই পর্দার মধ্যে সুড়ঙ্গ করে সবুজ অংশ খেয়ে ফেলে। অনেকগুলো কীড়া এভাবে খাওয়ার ফলে পাতা শুকিয়ে যায়। কীড়া এবং  পুত্তলীগুলো সুড়ঙ্গের মধ্যেই থাকে। পামরী পোকা  ৩-৪ সপ্তাহের মধ্যে জীবন চক্র সম্পূর্ণ করে। <br>

<a2>দমন ব্যবস্থাপনা:</a2>
হাতজাল বা গামছা দিয়ে পোকা ধরে মেরে ফেলুন।<br>
গাছে কুশী ছাড়ার শেষ সময় পর্যন্ত পাতার গোড়ার ২-৩ সেমি (প্রায় ১ ইঞ্চি) উপর থেকে ছেটে দিয়ে শতকরা ৭৫-৯২টা পামরী পোকার কীড়া  মেরে ফেলা যায় এবং পরবর্তী আক্রমণ রোধ করা যায়। <br>
শতকরা ৩৫ ভাগ পাতার ক্ষতি হলে অথবা প্রতি গোছা ধান গাছে ৪টি পূর্ণবয়স্ক পোকা থাকলে অনুমোদিত কীটনাশক ব্যবহার করুন।</a2><br>
