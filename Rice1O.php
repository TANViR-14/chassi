দমন পদ্ধতি হলো, হাত / নিড়ানি / রাইস উইডারের সাহায্যে দমন করতে হবে। অথবা চারা রোপনের ৭-১৫ দিনের মধ্যে সুপার পাওয়ার ২০ গ্রম ৩৩ শতক জমিতে প্রয়োগ করতে হবে।</a2>

